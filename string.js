

/* concat() */ 
/* combine two or more string object */
var str1 = "Hello ";
var str2 = "world!";
var res = str1.concat(str2);
// => Hello words !

/* toUpperCase() */ 
/* make capitalize */
var str = "Hello World!";
var res = str.toUpperCase();
// => HELLO WORLD!

/* toLowerCase() */ 
/* Convert the string to lowercase letters */
var str = "Hello World!";
var res = str.toLowerCase();
// => hello world! !

